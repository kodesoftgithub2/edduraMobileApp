package com.eddura.pojo;

public class SpecificUser_Classes {

	public String id;
	public String name;
	public String department_id;
	public String department_name;
	public String class_teacher_name;
	public String subjects_count;
	public String students_count;
	public String is_published;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDepartment_id() {
		return department_id;
	}
	public void setDepartment_id(String department_id) {
		this.department_id = department_id;
	}
	public String getDepartment_name() {
		return department_name;
	}
	public void setDepartment_name(String department_name) {
		this.department_name = department_name;
	}
	public String getClass_teacher_name() {
		return class_teacher_name;
	}
	public void setClass_teacher_name(String class_teacher_name) {
		this.class_teacher_name = class_teacher_name;
	}
	public String getSubjects_count() {
		return subjects_count;
	}
	public void setSubjects_count(String subjects_count) {
		this.subjects_count = subjects_count;
	}
	public String getStudents_count() {
		return students_count;
	}
	public void setStudents_count(String students_count) {
		this.students_count = students_count;
	}
	public String getIs_published() {
		return is_published;
	}
	public void setIs_published(String is_published) {
		this.is_published = is_published;
	}
	

}
